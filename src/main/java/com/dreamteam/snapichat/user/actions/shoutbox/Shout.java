/**
 *
 * @author Lefteris
 */
public class Shout {
    
    private String username;
    private String text;

    public Shout() {
    }
    
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
    
    
    
}
